SELECT NUMPED,QT,PVENDA,PTABELA,VLCUSTOFIN,PERCOM,PERDESC,CODICMTAB IMPOSTO,SUM(QT*PVENDA)VLTOTAL,
ROUND(((sum(qt*pvenda)-sum(qt*vlcustofin))/(sum(qt*pvenda))*100),2) lucro
FROM PCPEDI 
WHERE NUMPED IN (6600014081,1020000527,2200005178) 
AND CODPROD = 261 
GROUP BY
NUMPED,QT,PVENDA,PTABELA,VLCUSTOFIN,PERCOM,PERDESC,CODICMTAB
